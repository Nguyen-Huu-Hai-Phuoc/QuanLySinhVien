    void init() {
        setLocationRelativeTo(null);
        filltable();
        updateStatus();
    }

    public void filltable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.select();
            for (NhanVien nv : list) {
                 
                Object[] row = {
                    nv.getMaNV(),
                    makePasswordInvisible(nv.getMatKhau()),
                    nv.getHoTen(),
                    nv.isVaiTro() ? "Trưởng phòng" : "Nhân viên",
//                    nv.getGioiTinh()
                };
                model.addRow(row);
            }
            
        } catch (Exception e) {
            DialogHelPer.alert(this, "Lõi truy cập dữ liệu!");
        }
    }

    public void edit() {
        try {
            String maNV = (String) tblNhanVien.getValueAt(this.row, 0);
            NhanVien model = dao.findById(maNV);
            if (model != null) {
                setForm(model);
                updateStatus();
                pnlDanhSach.setSelectedIndex(0);

            }
        } catch (Exception e) {
            DialogHelPer.alert(this, "Lỗi truy cập dữ liệu!");
        }
    }
