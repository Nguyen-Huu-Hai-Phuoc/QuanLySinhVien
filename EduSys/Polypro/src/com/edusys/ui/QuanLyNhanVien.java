    public void insert() {
        NhanVien model = getForm();
        String confirm = new String(txtXNMatKau.getPassword());
        if (confirm.equals(model.getMatKhau())) {
            try {
                dao.insert(model);
                this.filltable();
                this.clearForm();
                DialogHelPer.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
//                DialogHelPer.alert(this, "Thêm mới thất bại!");
                e.printStackTrace();
            }
        } else {
            DialogHelPer.alert(this, "Xác nhận mật khẩu không đúng!");
        }
    }

    public void update() {
        NhanVien model = getForm();
        String confirm = new String(txtXNMatKau.getPassword());
        if (!confirm.equals(model.getMatKhau())) {
            DialogHelPer.alert(this, "Xác nhận mật khẩu không đúng!");
        } else {
            try {
                dao.update(model);
                this.filltable();
                DialogHelPer.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                DialogHelPer.alert(this, "Cập nhật thất bại!");
            }
        }
    }

    public void delete() {
        if (!Auth.isManager()) {
            DialogHelPer.alert(this, "Bạn không có quyền xóa nhân viên!");
        } else {
            if (DialogHelPer.confirm(this, "Bạn thực sự muốn xóa nhân viên này?")) {
                String manv = txtMaNV.getText();
                try {
                    dao.delete(manv);
                    this.filltable();
                    this.clearForm();
                    DialogHelPer.alert(this, "Xóa nhân viên thành công!");
                } catch (Exception e) {
                    DialogHelPer.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    public void first() {
        row = 0;
        edit();
    }

    public void prev() {
        if (row > 0) {
            row--;
            edit();
        }
    }

    public void next() {
        if (row < tblNhanVien.getRowCount() - 1) {
            row++;
            edit();
        }
    }

    public void last() {
        row = tblNhanVien.getRowCount() - 1;
        edit();
    }

    //Ẩn mật khẩu
    private String makePasswordInvisible(String pass) {
        String hide = "";
        for (int i = 0; i < pass.length(); i++) {
            hide += "*";
        }
        return hide;
    }
